	<h1>Ochrana osobních údajů a cookies</h1>

	<h2>Analytics cookies</h2>
	<p>
		Na našem webu používáme <b>Google Analytics</b> k pochopení, jak návštěvníci používají naše
		stránky. Google Analytics nastavuje cookies, jako jsou <code>_ga</code> a
		<code>_gid</code>, aby shromažďoval <b>anonymizovaná data o návštěvnosti</b>, například které
		stránky jsou navštěvovány a jak dlouho na nich uživatelé zůstávají.
	</p>

	<ul>
		<li>
			<b>Shromažďovaná data:</b> pouze anonymizovaná statistika používání,
			<b>žádné osobní údaje</b>.
		</li>
		<li><b>Účel:</b> zlepšování webu a uživatelského zážitku.</li>
		<li>
			<b>Souhlas:</b> tyto cookies jsou <b>volitelné</b> a nastaví se pouze, pokud udělíte svůj souhlas.
		</li>
		<li>
			<b>Odvolání souhlasu:</b> souhlas můžete kdykoli odvolat odstraněním dat uložených ve vašem
			prohlížeči pro tento web (včetně <code>localStorage</code>) nebo úpravou nastavení cookies ve
			vašem prohlížeči.
		</li>
	</ul>

	<p>
		Pokud s cookies neodsouhlasíte, Google Analytics se nenačte a žádná analytická data se nebudou
		shromažďovat.
	</p>
